msg = "Warning! "
print(msg.upper())        # Output: WARNING!  {upper case}

print(msg.lower())       # Output: warning!  {lower case}

print(msg.strip())       # Output: Warning!  {removes whitespace from start and end}

print(msg.replace("Warning", "Alert"))  # Output: Alert!  {replaces substring}

print(len(msg))             # Output: 9  {length of the string}